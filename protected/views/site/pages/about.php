transportes Unión Cariamanga”. Desde que se consolidó como “Cooperativa de Transportes Unión Cariamanga”, su misión ha sido prestar un servicio de transporte eficiente, cómodo<br>
y seguro, que cubra con calidad y rentabilidad las rutas establecidas, logrando relaciones armoniosas con los  clientes y colaboradores, cumpliendo siempre con las leyes vigentes,<br>
<br>así como también proyectándose al desarrollo social y económico de la Provincia y del País.
